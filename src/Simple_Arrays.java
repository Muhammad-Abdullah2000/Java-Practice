public class Simple_Arrays {
    public static void main(String[] args) {


        // By simple Array method

        System.out.println("Array by simple method");

        int[] marks = {98, 45, 79, 99, 80};
        System.out.println(marks[2]);

        System.out.println("Length of a array");
        System.out.println(marks.length);


        // By for each_Loop

        System.out.println("By For Each Loop Method");

        int[] marks_1 = {98, 45, 79, 99, 80};
        for (int i : marks_1) {
            System.out.println(i);




        }
    }
    }

